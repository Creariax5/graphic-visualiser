package org.example.tool;

public class Vector {
    final double toX;

    public double getToY() {
        return toY;
    }

    public double getToX() {
        return toX;
    }

    public Vector(double toX, double toY) {
        this.toX = toX;
        this.toY = toY;
    }

    final double toY;
}
